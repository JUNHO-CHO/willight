# 구구단 출력for for

def gugu():
    for i in range(2, 10):   #시작값 2, 종료 9 범위 설정 후 반목문 설정
        print('')            #줄바꿈
        for j in range(1, 10):    # 시작 1, 종료 9 범위 설정 후 반복
            print(f'{i}x{j}={i*j}')   # ixj= ij 형식으로 프린트 하도록 설정
            return
print(gugu())                         # 출력
